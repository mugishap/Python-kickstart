def add(a, b):
    return "The sum is {}".format(a + b)
 
def subtract(a, b):
    return "Subtracting {} from {} gives {}".format(b, a, a - b)


def multiply(a, b):
    return "The product of {} and {} is: {}".format(a, b, a * b)


def divide(a, b):
    return "The quotient of {} and {} ({}/{}) is: {}".format(a, b, a, b, a / b)
